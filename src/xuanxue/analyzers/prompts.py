CHENGGU_ANALYSIS_PROMPT = """你是一位解读袁天罡称骨算命结果的分析师。

## 输入数据

骨重计算结果：
{chenggu_result}

## 输出要求

基于骨重和歌诀，给出简短解读：

1. **骨重说明**：{total_display}，等级：{grade}
2. **歌诀白话**：将歌诀翻译成通俗易懂的语言
3. **六维度概括**：
   - 性格/事业/财运/关系/身心/阶段各一句话

## 约束

- 称骨是民俗文化，不是科学预测
- 不要说"注定""必然"
- 最后提醒：称骨算命仅供娱乐参考
"""

METHOD_PROMPTS: dict[str, str] = {
    "bazi": BAZI_ANALYSIS_PROMPT,
    "ziwei": ZIWEI_ANALYSIS_PROMPT,
    "qimen": QIMEN_ANALYSIS_PROMPT,
    "chenggu": CHENGGU_ANALYSIS_PROMPT,
}


def get_analysis_prompt(method: str, **kwargs: str) -> str:
    """获取方法的分析 Prompt"""
    template = METHOD_PROMPTS.get(method)
    if template is None:
        return f"请分析以下数据：{kwargs}"
    try:
        return template.format(**kwargs)
    except KeyError:
        return template
