_HOUR_TO_ZHI: list[tuple[int, int, str]] = [
    (23, 1, "子"), (1, 3, "丑"), (3, 5, "寅"), (5, 7, "卯"),
    (7, 9, "辰"), (9, 11, "巳"), (11, 13, "午"), (13, 15, "未"),
    (15, 17, "申"), (17, 19, "酉"), (19, 21, "戌"), (21, 23, "亥"),
]


def _hour_to_zhi(hour: int, minute: int = 0) -> str:
    t = hour + minute / 60.0
    if t >= 23.0:
        return "子"
    for start, end, zhi in _HOUR_TO_ZHI:
        if start <= t < end:
            return zhi
    return "子"


def _qian_to_liang(qian: int) -> str:
    liang = qian // 10
    qian_rem = qian % 10
    if liang > 0 and qian_rem > 0:
        return f"{liang}两{qian_rem}钱"
    elif liang > 0:
        return f"{liang}两"
    else:
        return f"{qian_rem}钱"


