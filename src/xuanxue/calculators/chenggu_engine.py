def calculate_chenggu(input_data: dict[str, Any]) -> dict[str, Any]:
    """称骨算命计算"""
    bd = input_data["birth_datetime"]
    parts = bd.replace("/", "-").split()
    date_parts = parts[0].split("-")
    month = int(date_parts[1])
    day = int(date_parts[2])

    time_str = parts[1] if len(parts) > 1 else "12:00"
    time_parts = time_str.split(":")
    hour = int(time_parts[0])
    minute = int(time_parts[1]) if len(time_parts) > 1 else 0

    if hour >= 23:
        day += 1
        if day > 30:
            day = 1

    hour_zhi = _hour_to_zhi(hour, minute)
    year_w = 10
    month_w = MONTH_WEIGHT.get(month, 0)
    day_w = DAY_WEIGHT.get(day, 0)
    hour_w = HOUR_WEIGHT.get(hour_zhi, 0)
    total_qian = year_w + month_w + day_w + hour_w

    verse_info = VERSE_TABLE.get(total_qian)
    if verse_info is None:
        closest = min(VERSE_TABLE.keys(), key=lambda x: abs(x - total_qian))
        verse_info = VERSE_TABLE[closest]

    verse, grade = verse_info
    total_display = _qian_to_liang(total_qian)

    return {
        "weights": {"year": year_w, "month": month_w, "day": day_w, "hour": hour_w},
        "total_qian": total_qian,
        "total_display": total_display,
        "grade": grade,
        "verse": verse,
    }
